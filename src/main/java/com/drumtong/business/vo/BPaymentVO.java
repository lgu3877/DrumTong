package com.drumtong.business.vo;

public class BPaymentVO {
	private String estid;
	private String bank;
	private String accountnum;
	private String copyofbankbook;
	private String card;
	public String getEstid() {
		return estid;
	}
	public void setEstid(String estid) {
		this.estid = estid;
	}
	public String getBank() {
		return bank;
	}
	public void setBank(String bank) {
		this.bank = bank;
	}
	public String getAccountnum() {
		return accountnum;
	}
	public void setAccountnum(String accountnum) {
		this.accountnum = accountnum;
	}
	public String getCopyofbankbook() {
		return copyofbankbook;
	}
	public void setCopyofbankbook(String copyofbankbook) {
		this.copyofbankbook = copyofbankbook;
	}
	public String getCard() {
		return card;
	}
	public void setCard(String card) {
		this.card = card;
	}
	
	
}
